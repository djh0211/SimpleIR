
class Calc:
    def sum(self, a, b):
        result = a + b
        print("{0} + {1} = {2} 입니다.".format(a, b, result))

    def sub(self, a, b):
        result = a - b
        print("{0} - {1} = {2} 입니다.".format(a, b, result))

    def multi(self, a, b):
        result = a * b
        print("{0} * {1} = {2} 입니다.".format(a, b, result))

    def divi(self, a, b):
        result = a / b
        print("{0} / {1} = {2} 입니다.".format(a, b, result))


